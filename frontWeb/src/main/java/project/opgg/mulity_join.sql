SELECT gh.GAMEID,gh.GAMECODE, gh.TEAMCOLOR,gh.ISWIN,gh.USERID,gh.KILL,gh.DEATH,gh.ASSIST,gh.DAMAGE, 
ui.UTIER , ci.CHAMIMG,ri.RUNEIMG ,si.SPELLIMG ,ii.ITEMIMG 
FROM GAME_HISTORY gh,USER_INFO ui,CHAM_INFO ci,RUNE_INFO ri,SPELL_INFO si,ITEM_INFO ii
WHERE gh.USERID = ui.USERID AND gh.CHAMID = ci.CHAMID AND gh.RUNEID = ri.RUNEID AND gh.SPELLID =si.SPELLID AND gh.ITEMID = ii.ITEMID
AND gh.gamecode='솔로랭크-1' ORDER BY gameid; 


